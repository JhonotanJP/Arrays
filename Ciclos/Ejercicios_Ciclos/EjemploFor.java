public class EjemploFor {

    public static void main(String[] args) {
        // El ciclo for inicializa la variable i en 1,
        // se ejecuta mientras i sea menor o igual a 5,
        // y después de cada iteración incrementa i en 1.
        for (int i = 1; i <= 5; i++) {
            System.out.println("i es: " + i);
        }

        System.out.println("Ciclo for terminado");
    }
}
